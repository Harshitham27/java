class LongRunner
{
	public static void main(String[] args)
	{
		long noOfStars=40000000000L;
		System.out.println("No of Stars:"+noOfStars);
		
		long ageOfUniverse=1310000000000L;
		System.out.println("ageOfUniverse:"+ageOfUniverse);
		
		long number=8951201873L;
		System.out.println("number:"+number);
		
		long noOfTrees=2200000000L;
		System.out.println("No of Trees:"+noOfTrees);
		
		long noOfPassengers=1000000000L;
		System.out.println("no of passengers:"+noOfPassengers);
		
		long noOfSmartphones=2000000000L;
		System.out.println("No of Smartphones:"+noOfSmartphones);
		
		long noOfBooks=2230000000L;
		System.out.println("No of Books:"+noOfBooks);
		
		long noOfVehicles=12300000000L;
		System.out.println("No of Vehicles:"+noOfVehicles);
		
		long noOfAirlinePassengers=2240000000L;
		System.out.println("No of Airline passengers:"+noOfAirlinePassengers);
		
		long distanceToMoon=380000000L;
		System.out.println("Distance to moon from earth:"+distanceToMoon);
	}
}